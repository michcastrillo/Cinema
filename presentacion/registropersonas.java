    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int op = generocombo.getSelectedIndex();
        if (op!=0) {
            String cedula = cedulatxt.getText();
            boolean comprobacion = desapersonas.comprobarcedula(cedula);
            if (comprobacion==true){
                String nombre = nombretxt.getText();
                boolean compro = desapersonas.comprobarnombre(nombre);
                if(compro==true){
                    String genero = SeleccionGenero();
                    personas.listapersonas.add(new personas(cedula, nombre, genero));
                    desapersonas.guardarProducto();
                    mostrarDatosObjeto();
                }else{
                   JOptionPane.showMessageDialog(null, "Digite un nombre", "Mensaje de error",
                        JOptionPane.ERROR_MESSAGE); 
                }
            }else{
                JOptionPane.showMessageDialog(null, "Ingrese una cédula diferente", "Mensaje de error",
                   JOptionPane.ERROR_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Elija un género", "Mensaje de error",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

