    public void mostrarDatosObjeto() {
        DefaultTableModel model = (DefaultTableModel) tabla.getModel();
        model.setRowCount(0);
        Object[] arreglo = new Object[3];
        for (int i = 0; i < personas.listapersonas.size(); i++) {
            arreglo[0] = personas.listapersonas.get(i).getCedula();
            arreglo[1] = personas.listapersonas.get(i).getNombre();
            arreglo[2] = personas.listapersonas.get(i).getGenero();
            model.addRow(arreglo);
        }
    }
